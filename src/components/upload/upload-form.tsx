"use client";

import {
    generatePdfSummary,
    generatePdfText,
    storePdfSummaryAction,
} from "@/actions/upload-actions";
import addUploadToCount from "@/actions/user-actions";
import UploadFormInput from "@/components/upload/upload-form-input";
import { formatFileNameAsTitle } from "@/utils/format-utils";
import { useUser } from "@clerk/nextjs";
import { useRouter } from "next/navigation";
import { useRef, useState } from "react";
import { toast } from "sonner";
import { z } from "zod";
import { useUploadThing } from "../../utils/uploadthing";
import LoadingSkeleton from "./loading-skeleton";

const schema = z.object({
    file: z
        .instanceof(File, { message: "Invalid file" })
        .refine(
            (file) => file.size <= 24 * 1024 * 1024,
            "File must be less than 24MB",
        )
        .refine(
            (file) => file.type === "application/pdf",
            "File must be a PDF",
        ),
});

export default function UploadForm() {
    const [isLoading, setIsLoading] = useState(false);
    const router = useRouter();
    const { user } = useUser();

    const formRef = useRef<HTMLFormElement>(null);
    const { startUpload, routeConfig } = useUploadThing("pdfUploader", {
        onClientUploadComplete: () => {
            console.log("Upload completed");
        },
        onUploadError: (error: Error) => {
            console.error(`Error uploading file! ${error.message}`);
        },
        onUploadBegin: () => {
            console.log("Uploading...");
        },
    });

    const handleSubmit = async (e: React.FormEvent<HTMLFormElement>) => {
        e.preventDefault();

        try {
            setIsLoading(true);
            console.log("submitted");
            const formData = new FormData(e.currentTarget);
            const file = formData.get("file") as File;

            // Validate file
            const validatedFields = schema.safeParse({ file });
            if (!validatedFields.success) {
                const errorMessage =
                    validatedFields.error.errors?.[0]?.message || "Something went wrong!";
                toast.error(errorMessage);
                setIsLoading(false);
                return;
            }

            toast.info("ðŸ“„ Uploading file...");

            const response = await startUpload([file]);
            if (!response) {
                toast.error(
                    "Something went wrong! Please use a valid/different PDF file.",
                );
                setIsLoading(false);
                return;
            }

            toast.info("File uploaded! Our AI is reading your file... âœ¨");

            let storeResult: any;

            const formattedFileName = formatFileNameAsTitle(file.name);

            const result = await generatePdfText(response[0].serverData.file.url);

            toast.info("Generating PDF summary... âœ¨");

            const summaryResult = await generatePdfSummary({
                pdfText: result?.data?.pdfText ?? "",
                fileName: formattedFileName,
            });

            toast.info("Summary generated! âœ¨");

            const { data = null } = summaryResult || {};

            if (data?.summary) {
                toast.info("Saving your summary... âœ¨");

                storeResult = await storePdfSummaryAction({
                    fileUrl: response[0].serverData.file.url,
                    summary: data.summary,
                    title: formattedFileName,
                    fileName: file.name,
                });

                toast.success("âœ¨ Summary successfully generated!");

                const primaryEmail = user?.emailAddresses?.[0]?.emailAddress;
                if (primaryEmail) {
                    await addUploadToCount(primaryEmail);
                }

                formRef.current?.reset();
                router.push(`/summaries/${storeResult.data.id}`);
            }
        } catch (error) {
            console.error("Error uploading file", error);
            formRef.current?.reset();
        } finally {
            setIsLoading(false);
        }
    };

    return (
        <div className="flex flex-col gap-6 w-full max-w-2xl mx-auto">
            <UploadFormInput
                isLoading={isLoading}
                ref={formRef}
                onSubmit={handleSubmit}
            />
            {isLoading && (
                <>
                    <div className="relative">
                        <div
                            className="absolute inset-0 flex items-center justify-center"
                            aria-hidden="true"
                        >
                            <div className="w-full border-t border-gray-200 dark:border-gray-800"></div>
                        </div>
                        <div className="relative flex justify-center mt-4">
                            <span className="bg-transparent px-3 py-1.5 text-muted-foreground text-sm">
                                Processing
                            </span>
                        </div>
                    </div>
                    <LoadingSkeleton />
                </>
            )}
        </div>
    );
}
